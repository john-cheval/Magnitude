import "./globals.css";

import { ToastContainer } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import Navbar from "@/components/common/Navbar";
import Footer from "@/components/common/Footer";
import { fetchData } from "@/utils/fetchData";
import { baseUrl } from "@/utils/apiUrl";
import Script from "next/script";

export const metadata = {
  title: "Magnitude",
  description: "Generated by create next app",
};

export default async function RootLayout({ children }) {
  const [navDataLeft, navDataRight, mobileMenu, footerData] = await Promise.all(
    [
      fetchData(`${baseUrl}/menu/primary`),
      fetchData(`${baseUrl}/menu/primaryright`),
      fetchData(` ${baseUrl}/menu/mobilemenu`),
      fetchData(`${baseUrl}/footer_details`),
    ]
  );
  return (
    <html
      lang="en"
      suppressHydrationWarning={true}
      className="notranslate"
      translate="no"
    >
      <body className={` antialiased flex flex-col min-h-screen`}>
        <Navbar
          navLeft={navDataLeft?.menu_tree}
          navRight={navDataRight?.menu_tree}
          mobileMenu={mobileMenu?.menu_tree}
        />
        <main className="flex-grow">
          {children}
          <ToastContainer />
        </main>
        <Footer data={footerData} />
        <Script
          src="https://www.googletagmanager.com/gtag/js?id=G-BRP760DJM3"
          strategy="afterInteractive"
        />
        <Script id="google-analytics" strategy="afterInteractive">
          {`
            window.dataLayer = window.dataLayer || [];
            function gtag(){dataLayer.push(arguments);}
            gtag('js', new Date());
            gtag('config', 'G-BRP760DJM3');
          `}
        </Script>
      </body>
    </html>
  );
}
